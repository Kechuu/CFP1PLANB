package modelo;

/**
 *
 * @author araa
 */
public class Lugar {
    
    private int idLugar;
    private String nombre;
    private int nivel;
    private Lugar de;

    public int getIdLugar() {
        return idLugar;
    }

    public void setIdLugar(int idLugar) {
        this.idLugar = idLugar;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getNivel() {
        return nivel;
    }

    public void setNivel(int nivel) {
        this.nivel = nivel;
    }

    public Lugar getDe() {
        return de;
    }

    public void setDe(Lugar de) {
        this.de = de;
    }
    
    
    
}
