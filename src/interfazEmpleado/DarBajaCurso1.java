    private void btnDarBajaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDarBajaActionPerformed

        //DefaultListModel lista=(DefaultListModel) listDisponible.getModel();
        if(listaEmpleados.getSelectedIndex()==-1){
            JOptionPane.showMessageDialog(null, "Necesita seleccionar a un empleado para dar de baja");
        }else{
            if(listDisponible.getSelectedIndex()==-1){
                JOptionPane.showMessageDialog(null, "Seleccione el curso que desea dar de baja");
            }else{
                int idCurso = ctrlCurso.leer(listDisponible.getSelectedValue().getIdTipoCurso()).getIdCurso();
                int idEmpleado = ctrlEmpleado.leer(listaEmpleados.getSelectedValue().getIdPersona()).getIdEmpleado();
                ctrlCursoProfesor.borrar(idEmpleado, idCurso); 
                
                modeloDisponible.removeAllElements();
                listaEmpleados.clearSelection();
            }
        }

    }//GEN-LAST:event_btnDarBajaActionPerformed

    private void listaEmpleadosValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listaEmpleadosValueChanged
        // TODO add your handling code here:
        if(listaEmpleados.getSelectedIndex()!=-1){
            if(contador==0){
                contador=1;
                //cbCargo.removeAllItems();
            }else{
                int idEmpleado=ctrlEmpleado.leer(listaEmpleados.getSelectedValue().getIdPersona()).getIdEmpleado();
                contador=0;

                ctrlCursoProfesor.llenarLista(idEmpleado, listDisponible);
            }
        }
    }//GEN-LAST:event_listaEmpleadosValueChanged


