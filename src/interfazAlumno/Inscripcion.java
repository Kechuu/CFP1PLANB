    private void btnSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSiguienteActionPerformed
        switch(panelContenedor.getSelectedIndex()){
            case 0:
                
                if(cbTipo.getSelectedIndex()==0 || txtDni.getText().equals("") || txtCuil.getText().equals("") || txtNombre.getText().equals("")
                    || txtApellido.getText().equals("") || cbNacimiento.getSelectedIndex()==0 || cbNacionalidad.getSelectedIndex()==0){
            
                    JOptionPane.showMessageDialog(null, "Faltan datos qu챕 ingresar");
                }else{
                    panelContenedor.setEnabledAt(1, true);
                    panelContenedor.setSelectedIndex(1);
                    panelContenedor.setEnabledAt(0, false);
                    
                    btnAtras.setEnabled(true);
                }
                
            break;
            
            case 1:
                if(contadorGuardar==0){
                    if (cbLocalidad.getSelectedIndex() == 0 || cbBarrio.getSelectedIndex() == 0 || cbCalle.getSelectedIndex() == 0 || txtCasa.getText() == null || txtCasa.getText().equals("")) {
                        JOptionPane.showMessageDialog(null, "Le falta completar domicilio: ingresar n째 de casa o seleccionar localidad, barrio y/o calle.");
                        
                    } else {

                        if(txtCelular.getText().equals("")||txtFijo.getText().equals("")||txtCorreo.getText().equals("")){
                         
                            if (JOptionPane.showConfirmDialog(null, "Hay campos vac챠os, 쩔Quiere segeuir sin agregarlos?", "", +JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {

                                panelContenedor.setEnabledAt(2, true);
                                panelContenedor.setSelectedIndex(2);
                                panelContenedor.setEnabledAt(1, false);

                            }
                        }else{
                            panelContenedor.setEnabledAt(2, true);
                            panelContenedor.setSelectedIndex(2);
                            panelContenedor.setEnabledAt(1, false);

                        }
                    }  
                    
                }else{//CUANDO YA SE HA DADO DE ALTA UNA PERSONA Y SOLO EST 'NAVEGANDO' POR LAS INTERFACES
                    panelContenedor.setEnabledAt(2, true);
                    panelContenedor.setSelectedIndex(2);
                    panelContenedor.setEnabledAt(1, false);
                }
                
            break;
            
            case 2:
                if(contadorGuardar==0){
                    btnSiguiente.setText("Terminar");
                    guardar();
                    
                }else{
                    btnSiguiente.setEnabled(false);
                }
            break;
            
                
        }
    }//GEN-LAST:event_btnSiguienteActionPerformed

