    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        if(cbMotivoBaja.getSelectedIndex()==0 && txtDetalle.getText().equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(null, "Necesita seleccionar o ingresar un motivo de baja");
            return;
        }else{
            if(!txtDetalle.getText().equals("")){
                ctrlMotivo.crear(txtDetalle.getText());
                motivoB=ctrlMotivo.leer();
            }else{
                motivoB=(MotivoBaja) cbMotivoBaja.getSelectedItem();                
            }
        }
    }//GEN-LAST:event_btnAceptarActionPerformed


